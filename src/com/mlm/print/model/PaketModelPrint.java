package com.mlm.print.model;

public class PaketModelPrint {
	private String code;
	private String nama;
	private String harga;
	private String ket;
	private String waktu;
	private String status;
	private String totalPelanggan;
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getNama() {
		return nama;
	}
	public void setNama(String nama) {
		this.nama = nama;
	}
	public String getHarga() {
		return harga;
	}
	public void setHarga(String harga) {
		this.harga = harga;
	}
	public String getKet() {
		return ket;
	}
	public void setKet(String ket) {
		this.ket = ket;
	}
	public String getWaktu() {
		return waktu;
	}
	public void setWaktu(String waktu) {
		this.waktu = waktu;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTotalPelanggan() {
		return totalPelanggan;
	}
	public void setTotalPelanggan(String totalPelanggan) {
		this.totalPelanggan = totalPelanggan;
	}
	
}
